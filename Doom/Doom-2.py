class Caja3D(Button):
    def __init__(self, position=(0, 0, 0), escala=(0, 0), textura='white_cube'):
        super().__init__(
            parent=scene,
            position=position,
            model='cube',
            origin_y=0.5,
            collider='box',
            color=color.color(0, 0, random.uniform(0.9, 1)),
            scale=escala,
            texture=textura,  # Usar la textura proporcionada
            highlight_color=color.lime)


