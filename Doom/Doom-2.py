def crear_niveles(nivel1, nivel2):
    largoHabitacion = len(nivel1)
    anchoHabitacion = len(nivel1[0])
    # Crear un espacio, como una sala
    for z in range(largoHabitacion):
        piso = nivel1[z]
        techo = nivel2[z]
        # Crear el suelo y el techo
        for x in range(anchoHabitacion):
            if piso[x] == 0:
                # Crear bloque en el suelo
                Caja3D(position=(x, 0, z), escala=(1, 1), textura='suelo.jpeg')
                # Crear bloque en el techo
                Caja3D(position=(x, 5, z), escala=(1, 1), textura='techo.png')
            if piso[x] > 0:
                # Crear bloque en el suelo con la altura correspondiente
                Caja3D(position=(x, piso[x], z), escala=(
                    1, piso[x]), textura='muros.png')
            if techo[x] > 0:
                # Crear bloque en el techo con la altura correspondiente
                Caja3D(position=(x, piso[x] + techo[x],
                       z), escala=(1, techo[x]))
                # Cerrar los lados del techo
                if x == 0 or x == anchoHabitacion - 1 or z == 0 or z == largoHabitacion - 1:
                    # Crear bloque en los lados del techo
                    Caja3D(
                        position=(x, piso[x] + techo[x], z), escala=(1, techo[x]))


crear_niveles(ini_nivel, ini_techos)


